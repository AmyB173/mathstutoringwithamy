        <p>Every year in maths I watch students plough through every past paper and predicted paper they can get their hands on, and often they are still disappointed when the results come. Why? How is that even possible when they have put in so many hours?</p>

        <p>Let me tell you about one student, because he sums it up perfectly.</p>

        <h2>Two papers a week, and still a grade D</h2>

        <p>He came to me for <a href="/a-level" style="color: var(--accent); font-weight: 700; text-underline-offset: 3px;">A Level maths</a>. He was genuinely good at the subject and had a grade 8 at <a href="/GCSE" style="color: var(--accent); font-weight: 700; text-underline-offset: 3px;">GCSE</a> to show for it. But he came out of his Year 12 mocks with a D, went back after the summer, resat, and got a D again.</p>

        <p>"But," he told me, "I did every single paper. Two papers a week."</p>

        <p>So we did a bit of digging. It turned out that when he sat those papers at home, relaxed, with his phone next to him to check things, he was usually getting around 60%. That sounds reasonable, doesn't it? Then I asked him the important question: what did he do about the 40% he got wrong?</p>

        <p>He looked over the mark scheme, and if it kind of made sense, he moved on.</p>

        <p>That, right there, is the biggest problem I see.</p>

        <h2>The 40% is where the marks are</h2>

        <p>The part you get wrong is the most valuable thing in front of you. Knowing what you got wrong, understanding why you got it wrong, and having a plan to fix it: that is the whole point of doing the paper in the first place. Reading the mark scheme until it "kind of makes sense" is nowhere near enough. It feels like progress, but nothing has actually changed, so the same marks go missing next time.</p>

        <p>There are two problems hiding inside that comfortable 60% as well. Doing a paper at home, relaxed, with a phone to hand, is not the same as doing it under exam conditions. The real figure is almost certainly lower. And a mark you scrape by checking something on your phone is not a mark you own.</p>

        <p>So here is what I would do instead. If the question you got wrong was on logs, treat that as a signal that logs need work. Go back to the textbook and try the hardest logs questions you can find. Skim the worked examples and check there isn't something fundamental you had quietly forgotten. Make sure you know logs inside out. And do all of that before you go anywhere near the next paper.</p>

        <p>Past papers are diagnostic tools. They tell you about your skill and they show you where your revision needs to go. They are not the revision itself.</p>

        <h2>A paper is a tool, not a plan</h2>

        <p>Even when you do use them well, past papers should not be your only method. Not everything is tested every time, so if you only revise the topics that happened to come up and catch you out, you will leave gaps.</p>

        <p>I always suggest working from a checklist of the chapters and making sure every one gets revisited in the textbook, even if it is just a quick ten minutes as a refresher. Papers show you where you are weakest right now. The checklist makes sure nothing gets quietly skipped.</p>

