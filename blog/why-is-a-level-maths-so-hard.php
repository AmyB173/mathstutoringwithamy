        <p>I get asked this a lot, by students who are finding the step up harder than they expected, by parents watching a child who sailed through GCSE suddenly start to struggle, and every now and then by other tutors as well. The honest answer is that it isn't really that hard, or at least not as hard as its reputation suggests. But there are some specific reasons A Level Maths is so often the subject people point to as difficult, and once you understand what they are, most of them can be dealt with.</p>

        <h2>The algebra jump from GCSE</h2>

        <p>At <a href="/GCSE" style="color: var(--deep-purple); font-weight: 700; text-underline-offset: 3px;">GCSE Maths</a>, to get a good grade, you need to be good at algebra, but you don't need to be great at it. At A Level, it is all algebra, and every single GCSE algebra concept is expected to be understood at its hardest level from the moment you start.</p>

        <p>The problem is that, especially if you got a Grade 6 or 7, you can get away with not really learning much of the algebra you're going to need. Then you come to your first lesson where it's assumed you already know how to complete the square, for example, and you find yourself having to re-learn that from GCSE at the same time as taking on all the new concepts piled on top.</p>

        <h2>Rote learning doesn't cut it anymore</h2>

        <p>At <a href="/GCSE" style="color: var(--deep-purple); font-weight: 700; text-underline-offset: 3px;">GCSE</a>, you can often get away with rote learning concepts without truly understanding them. At A Level, this will only take you so far. The exams are designed to test your understanding, not just a regurgitation of facts. Every year when students sit their exams, someone will say "but I've never seen a question like that before", and that is exactly what the exams are designed to do. They ask questions to test the underlying concepts, and there are many different ways they can do this that aren't predictable. If you understand the concepts fully, you should be able to apply them to any type of question. If you've only rote learnt facts and question types, you will really struggle once an exam comes around.</p>

        <h2>It needs constant use to stay fresh</h2>

        <p>A Level Maths requires a very dedicated and consistent approach to revision. I'd compare learning maths to learning a language. It's a skill, not just a set of facts to memorise, and like any skill it needs to be practised regularly to improve. Think of playing the piano. You don't learn a piece once and then never touch it again until the recital. You practise regularly, you keep old pieces warm alongside new ones, and your fluency builds over time.</p>

        <div class="blog-pullquote">
          <p>You learn a topic in September, and that topic needs to be revisited at least once a month to keep it fresh. By the time the exam comes around, it should be solidly lodged in long-term memory, not a clunky, half-remembered idea you're trying to reconstruct from scratch.</p>
        </div>

        <p>What a lot of students do instead is study a topic, revise it for a mini-test, and then forget about it until the mocks come around. By that point it's rusty, and it takes a lot of mental energy to recall something that should feel automatic.</p>

        <h2>The commitment required for independent work</h2>

        <p>Most students simply don't do enough revision and independent work. I always recommend around 5 hours a week on top of lesson time, dedicated to revising and learning concepts. And I mean 5 hours of proper, focused revision, not half-revising while chatting to friends in the common room.</p>

        <p>Students often struggle to commit to this many hours, and it can't really be made up for by cramming in the few weeks before the exams.</p>

